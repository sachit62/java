// bits intro

public class intro {
    public static void main(String[] args) {
        // AND &
        System.out.println(5 & 6);

        // OR |
        System.out.println(5 | 6);

        // XOR ^
        System.out.println(5 ^ 6);

        // NOT ~
        System.out.println(~5);

        // left shift <<
        System.out.println(5<<2);

        // right shift >>
        System.out.println(6>>1);
    }
}
